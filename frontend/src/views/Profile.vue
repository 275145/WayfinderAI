      ElMessage.error(error.message || 'ÂØÜÁ†Å‰øÆÊîπÂ§±Ë¥•ÔºåËØ∑ÈáçËØï')
    } finally {
      passwordLoading.value = false
    }
  })
}

// ËøîÂõûÈ¶ñÈ°µ
